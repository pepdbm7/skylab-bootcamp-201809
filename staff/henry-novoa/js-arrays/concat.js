    
    function concat(arr, arr2) {
        var result = [];
        for(var i = 0 ; i<arr.length;i++){
           
                result[i] = arr[i];
            }
        for(var j=0;j< arr2.length;j++ ){
                
                
                result[i] = arr2[j];
       
                i++;
            }   
            return result;
        };
        
